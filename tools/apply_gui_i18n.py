def main():
    s = GUI.read_text(encoding="utf-8")
    ordered = sorted(REPL, key=lambda x: len(x[0]), reverse=True)
    for old, new in ordered:
        s = s.replace(old, new)
    GUI.write_text(s, encoding="utf-8")
    print("Updated", GUI)


if __name__ == "__main__":
    main()
